<div id="getting-support">
  <h2 class="page-header">Getting Support</h2>
  <p>
    Having trouble getting the API up and running? Something not working the way you think it should? I hate that
    I don't have time to answer support emails anymore. So here are some things you might wanna do:
  </p>
  <ol>
    <li>
      Make sure you've read the latest version of how to <a href="../get-started/">get started</a>.
      It's been updated recently to make the API even easier to use.
    </li>
    <li>
      Check out the <a href="http://stackoverflow.com/questions/tagged/xboxleaders">existing questions tagged as
      XboxLeaders</a> over on Stack Overflow. Other folks might have had the same question you've had.
    </li>
    <li>
      Can't find the answer to your question on Stack Overflow?
      <a href="http://stackoverflow.com/questions/ask?tags=xboxleaders">Ask a new question</a>, then
      <a href="mailto:support@xboxleaders.com">send me an email</a> and I might be able to take a look.
    </li>
  </ol>
</div>

<section id="reporting-bugs">
  <h2 class="page-header">Reporting Bugs</h2>
  <p>
    Found a problem with the API? Feel free to submit an issue on the GitHub project. But please keep the following in mind:
  </p>
  <ol>
    <li>Please be nice. XboxLeaders is a happy place.</li>
    <li>Please <a href="https://github.com/XboxLeaders/XboxLiveApi/search?type=Issues">search</a> to see if your icon request already exists.</li>
    <li>
      Before opening any issue, please read the generic <a href="https://github.com/necolas/issue-guidelines">issue
      guidelines</a>, by <a href="https://github.com/necolas/">Nicolas Gallagher</a>.
    </li>
    <li>
      After doing everything above, feel free to
      <a href="https://github.com/XboxLeaders/XboxLiveApi/issues/new">submit an issue</a>.
    </li>
  </ol>
</section>

<section id="requesting-new-features">
  <h2 class="page-header">Requesting New Features</h2>
  <p>
    New features mostly start as requests by the
    <a href="https://github.com/XboxLeaders/XboxLiveApi/issues">XboxLeaders community on GitHub</a>. Want to request a new
    feature? Here are some things to keep in mind:
  </p>
  <ol>
    <li>Please be nice. XboxLeaders is a happy place.</li>
    <li>Please <a href="https://github.com/XboxLeaders/XboxLiveApi/search?type=Issues">search</a> to see if your feature request already exists.</li>
    <li>
      Please and thank you if you include the following:
      <ul>
        <li>
          Title your <a href="https://github.com/XboxLeaders/XboxLiveApi/issues/new?title=Feature%20Request:%20Feature%20Name">new issue</a>
          <code>Feature request: Feature Name</code>.
        </li>
        <li>Include a few use cases for your requested feature. How do you plan on using it?</li>
        <li>Attach an paragraph or two that represent the idea you're going for.</li>
      </ul>
    </li>
  </ol>
</section>

<section id="submitting-pull-requests">
  <h2 class="page-header">Submitting Pull Requests</h2>
  Found a way to solve a bug in the API? Want to contribute new features? Here are a few things to remember:
  <ol>
    <li>Please submit all pull requests against *-wip branches.</li>
    <li>All pull requests submitted against master will be sumarily closed and this guide referenced.</li>
    <li>
      After doing everything above, feel free to
      <a href="https://github.com/XboxLeaders/XboxLiveApi/issues/new">submit a pull request</a>.
    </li>
  </ol>
</section>

